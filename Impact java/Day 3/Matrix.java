public class Matrix{
    public static void main(String[] args){
        int x =3;
        int y =3;
        int n =1;
        for(int i=0;i<x;i++){
            for(int j=0;j<y;j++){
                System.out.print(" "+n+" ");
                n=n+1;
            }
            System.out.println();
        }
    }
}